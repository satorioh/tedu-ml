"""
数组的切片
"""
import numpy as np

a = np.arange(1, 19).reshape(2, 9)
print(a)
# 二维数组[行的操作，列的操作]
# 第0行的前2列
print(a[:1, :2])  # 二维[[1 2]]
print(a[0, :2])  # 一维[1 2]，索引降维了
print("==" * 20)

b = np.arange(1, 28).reshape(3, 3, 3)
print(b)
# 切出第2页
print(b[1, :, :])  # 二维[[10 11 12][13 14 15][16 17 18]]
# 切出所有页的第2行
print(b[:, 1, :])  # 二维[[ 4  5  6][13 14 15][22 23 24]]
# 切出第1页的所有行的第2列
print(b[0, :, 1])  # 一维[2 5 8]
